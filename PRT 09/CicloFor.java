public class CicloFor{
  public static void main(String args[]){

   for(int i = 1; i <= 5; i++){
       System.out.print(i + ", ");
  }
   System.out.println("");

   for(int a = 5; a >= 1; a--){
       System.out.print(a + ", ");
  }
   System.out.println("");

   for(int e = 2; e <= 10; e+=2){
       System.out.print(e + ", ");
  }   
   System.out.println("");

   for(int o = 25; o >= 5; o-=5){
       System.out.print(o + ", ");
  }   
   System.out.println("");

   for(int u = -1; u >= -5; u--){
       System.out.print(u + ", ");
  }
 }
}